# Convert a string variable to uppercase and lowercase.
message = str("welcome to python")

# Lowercase to Uppercase Converstion
print(message.upper()) # WELCOME TO PYTHON

# Uppercase to Lowercase Converstion
print(message.lower()) # welcome to python
 

# Captilized case
print(message.capitalize()) # Welcome to python

# Title Case
print(message.title()) # Welcome To Python
